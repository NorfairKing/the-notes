leftrightarrow :: Note
leftrightarrow = comm0 "leftrightarrow"

leftarrow :: Note
leftarrow = comm0 "leftarrow"

leftArrow :: Note
leftArrow = comm0 "Leftarrow"

rightarrow :: Note
rightarrow = comm0 "rightarrow"

rightArrow :: Note
rightArrow = comm0 "Rightarrow"

